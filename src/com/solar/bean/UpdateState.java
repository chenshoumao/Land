package com.solar.bean;

public class UpdateState {
	private String host;
	private String update_type;
	private String original_version;
	private String new_version;
	private String update_state;
	private String description;
	
	public String getHost() {
		return host;  
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getUpdate_type() {
		return update_type;
	}
	public void setUpdate_type(String update_type) {
		this.update_type = update_type;
	}
	public String getOriginal_version() {
		return original_version;
	}
	public void setOriginal_version(String original_version) {
		this.original_version = original_version;
	}
	public String getNew_version() {
		return new_version;
	}
	public void setNew_version(String new_version) {
		this.new_version = new_version;
	}
	public String getUpdate_state() {
		return update_state;
	}
	public void setUpdate_state(String update_state) {
		this.update_state = update_state;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
}
